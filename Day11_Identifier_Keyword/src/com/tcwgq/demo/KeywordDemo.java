package com.tcwgq.demo;
/**
 * Java中的关键字：
 * 就是使用高级编辑器时显示出特别颜色的单词，不多做解释！
 * 被Java语言赋予特定含义的单词
 * 组成关键字的字母全部小写！说明String不是关键字！
 */
public class KeywordDemo {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		String s = "Hello world!";//String不是Java中的关键字！
		System.out.println(s);
		System.out.println(a + b);
	}
}
